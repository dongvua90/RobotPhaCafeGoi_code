void PacketEditorScreen()
{
    char line[10];
    bool isUpdate = true;
    ssd1306_Fill(Black);
    ssd1306_SetCursor(25, 0);
    ssd1306_WriteString("PACKETS", Font_11x18, White);

    ssd1306_SetCursor(0, 20);
    ssd1306_WriteChar('<', Font_11x18, White);
    ssd1306_SetCursor(116, 20);
    ssd1306_WriteChar('>', Font_11x18, White);
    ssd1306_SetCursor(53, 20);
    snprintf(line, sizeof(line), "%u", (unsigned int)countPacket);
    ssd1306_WriteString(line, Font_11x18, White);

    ssd1306_SetCursor(0, 53);
    ssd1306_WriteString("* Enter / # back", Font_7x10, White);
    ssd1306_UpdateScreen();
    HAL_Delay(500);
	while(1)
	{
		if(BTN_UP_PRESS){
			countPacket++;
			if(countPacket > PACKET_MAX) countPacket = 1;
			isUpdate = true;
		}
		if(BTN_DOWN_PRESS){
			countPacket--;
			if(countPacket <=1) countPacket =PACKET_MAX;
			isUpdate = true;
		}

		if(BTN_ENTER_PRESS) return;
		if(BTN_BACK_PRESS) return;

		if(isUpdate)
		{
			isUpdate = false;
			ssd1306_SetCursor(53, 20);
			if(countPacket >=10){
				snprintf(line, sizeof(line), "%u ", (unsigned int)countPacket);
			}else{
				snprintf(line, sizeof(line), " %u", (unsigned int)countPacket);
			}
			ssd1306_WriteString(line, Font_11x18, White);
			ssd1306_UpdateScreen();
			HAL_Delay(100);
		}
	}
}

void WarningScreen(WarningState_t warning)
{
	ssd1306_Fill(Black);
	ssd1306_DrawBitmap(48, 0, icon_warning_31x28, 31, 28, White);
	ssd1306_SetCursor(14, 30);

	if(warning == LOW_WATER){
		ssd1306_WriteString("LOW WATER", Font_11x18, White);
		ssd1306_SetCursor(4, 50);
		ssd1306_WriteString(" REFILL WATER TANK ", Font_6x8, White);
	}else if(warning == NO_CUP){
		ssd1306_WriteString("PLACE CUP", Font_11x18, White);
		ssd1306_SetCursor(4, 50);
		ssd1306_WriteString("INSERT CUP TO START", Font_6x8, White);
	}else if(warning == NO_COFFEE){
		ssd1306_WriteString("NO COFFEE", Font_11x18, White);
		ssd1306_SetCursor(4, 50);
		ssd1306_WriteString("LOAD COFFEE PACKETS", Font_6x8, White);
	}
	ssd1306_SetCursor(0, 56);
	ssd1306_WriteString("#", Font_7x10, White);
	ssd1306_UpdateScreen();
	HAL_Delay(1000);
	while(1)
	{
		if(BTN_ENTER_PRESS || BTN_BACK_PRESS) return;
	}
}



