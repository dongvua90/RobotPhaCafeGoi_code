static inline const uint8_t *OLED_IconGetCafeLevelBitmap(OledCafeLevel_t level)
{
    switch (level) {
    case OLED_CAFE_LEVEL_LOW:
        return epd_bitmap_cafe_low;
    case OLED_CAFE_LEVEL_MID:
        return epd_bitmap_cafe_mid;
    case OLED_CAFE_LEVEL_HIGH:
    default:
        return epd_bitmap_cafe_high;
    }
}

static inline uint8_t OLED_IconMapY(uint8_t yTop, uint8_t iconHeight)
{
    if (iconHeight >= OLED_HEIGHT) {
        return 0U;
    }

    if ((uint16_t)yTop + iconHeight > OLED_HEIGHT) {
        yTop = (uint8_t)(OLED_HEIGHT - iconHeight);
    }

    return (uint8_t)(OLED_HEIGHT - iconHeight - yTop);
}

static inline void OLED_IconDrawCafeState(uint8_t x, uint8_t y, OledCafeLevel_t level, bool showSmoke, bool invert)
{
    uint8_t cupY;
    uint8_t smokeX;
    uint8_t smokeTopY;
    uint8_t smokeY;

    cupY = OLED_IconMapY(y, OLED_ICON_CAFE_H);
    OLED_DrawBitmap(x, cupY, OLED_ICON_CAFE_W, OLED_ICON_CAFE_H, OLED_IconGetCafeLevelBitmap(level), invert);

    if (!showSmoke) {
        return;
    }

    smokeTopY = (y > (OLED_ICON_SMOKE_H + 1U)) ? (uint8_t)(y - OLED_ICON_SMOKE_H - 1U) : 0U;
    smokeX = (uint8_t)(x + ((OLED_ICON_CAFE_W - OLED_ICON_SMOKE_W) / 2U));
    smokeY = OLED_IconMapY(smokeTopY, OLED_ICON_SMOKE_H);
    OLED_DrawBitmap(smokeX, smokeY, OLED_ICON_SMOKE_W, OLED_ICON_SMOKE_H, epd_bitmap_khoi_cafe, invert);
}

static inline void OLED_IconShowCafeState(uint8_t x, uint8_t y, OledCafeLevel_t level, bool showSmoke, bool invert, bool clearBefore, bool updateNow)
{
    if (clearBefore) {
        OLED_Clear();
    }

    OLED_IconDrawCafeState(x, y, level, showSmoke, invert);

    if (updateNow) {
        (void)OLED_Update();
    }
}

static inline void OLED_IconShowCafeLogoCentered(bool invert, bool updateNow)
{
    uint8_t x = (uint8_t)((OLED_WIDTH - OLED_ICON_CAFE_LOGO_W) / 2U);
    uint8_t yTop = (uint8_t)((OLED_HEIGHT - OLED_ICON_CAFE_LOGO_H) / 2U);
    uint8_t y = OLED_IconMapY(yTop, OLED_ICON_CAFE_LOGO_H);

    OLED_Clear();
    OLED_DrawBitmap(x, y, OLED_ICON_CAFE_LOGO_W, OLED_ICON_CAFE_LOGO_H, epd_bitmap_cafe, invert);

    if (updateNow) {
        (void)OLED_Update();
    }
}

