// Hàm hiển thị màn hình chính, nhấn up/down để chọn mức nước, nhấn * trả về 1, nhấn # trả về 2
int HomeScreen()
{
	ssd1306_Fill(Black);
	ssd1306_DrawBitmap(0,0,icon_cafe_mid_42x41,42,41,White);
	ssd1306_SetCursor(85, 0);
	ssd1306_WriteChar('^', Font_7x10, White);
	ssd1306_SetCursor(85, 32);
	ssd1306_WriteChar('v', Font_7x10, White);
	ssd1306_SetCursor(52, 11);
	ssd1306_WriteString("Medium", Font_11x18, White);
	ssd1306_SetCursor(0, 54);
	ssd1306_WriteString("* Brew / # Setting", Font_7x10, White);
	ssd1306_UpdateScreen();
	HAL_Delay(1000);
	while(1)
	{
		bool isUpdateLevelWate = false;
		if(BTN_UP_PRESS){
			levelWater++;
			if(levelWater>=3) levelWater = 0;
			isUpdateLevelWate = true;
		}
		if(BTN_DOWN_PRESS){
			levelWater--;
			if(levelWater <0) levelWater =2;
			isUpdateLevelWate = true;
		}

		if(BTN_ENTER_PRESS) return 1;
		if(BTN_BACK_PRESS) return 2;

		if(isUpdateLevelWate)
		{
			switch(levelWater)
			{
			case 0:
				ssd1306_FillRectangle(0, 0, 42, 41, Black);
				ssd1306_DrawBitmap(0,0,icon_cafe_low_42x41,42,41,White);
				ssd1306_SetCursor(52, 11);
				ssd1306_WriteString(" Small", Font_11x18, White);
				break;
			case 1:
				ssd1306_FillRectangle(0, 0, 42, 41, Black);
				ssd1306_DrawBitmap(0,0,icon_cafe_mid_42x41,42,41,White);
				ssd1306_SetCursor(52, 11);
				ssd1306_WriteString("Medium", Font_11x18, White);
				break;
			case 2:
				ssd1306_FillRectangle(0, 0, 42, 41, Black);
				ssd1306_DrawBitmap(0,0,icon_cafe_full_42x41,42,41,White);
				ssd1306_SetCursor(52, 11);
				ssd1306_WriteString(" Large", Font_11x18, White);
				break;
			}
			ssd1306_UpdateScreen();
			HAL_Delay(100);
		}
	}
}



// Hàm hiển thị các bước pha chế và tiến trình hoàn thành
void UI_Brewing(uint8_t step)
{
    ssd1306_Fill(Black);
    ssd1306_SetCursor(25, 0);
    ssd1306_WriteString("BREWING", Font_11x18, White);
    ssd1306_SetCursor(4, 27);
    ssd1306_WriteString((char*)s_brewStepName[step], Font_7x10, White);


    ssd1306_FillRectangle(0, 49, 128, 64, White);	// vẽ nền Progress
    if(step <5){
    	// độ dài pixel tiến trình  là 128-4=124, được chia ra thành 5 phần=> mỗi phần 25
    	uint8_t x = 2+step*25;
    	ssd1306_FillRectangle(x, 51, 126, 61, Black);   	// vẽ tiến trình thực tế
    }
    ssd1306_UpdateScreen();
}

